package com.cetnow.beans;

public class QuranSearch {
	private int Id;
	private int Aya;
	private String AyaClean;
	private String AyaUthmani;
	private String AyaUthmaniMin;
	private int Sura;
	private int AyaIndex;
	private String SuraName;
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public int getAya() {
		return Aya;
	}
	public void setAya(int aya) {
		Aya = aya;
	}
	public String getAyaClean() {
		return AyaClean;
	}
	public void setAyaClean(String ayaClean) {
		AyaClean = ayaClean;
	}
	public String getAyaUthmani() {
		return AyaUthmani;
	}
	public void setAyaUthmani(String ayaUthmani) {
		AyaUthmani = ayaUthmani;
	}
	public String getAyaUthmaniMin() {
		return AyaUthmaniMin;
	}
	public void setAyaUthmaniMin(String ayaUthmaniMin) {
		AyaUthmaniMin = ayaUthmaniMin;
	}
	public int getSura() {
		return Sura;
	}
	public void setSura(int sura) {
		Sura = sura;
	}
	public int getAyaIndex() {
		return AyaIndex;
	}
	public void setAyaIndex(int ayaIndex) {
		AyaIndex = ayaIndex;
	}
	public String getSuraName() {
		return SuraName;
	}
	public void setSuraName(String suraName) {
		SuraName = suraName;
	}
	
}
