package com.cetnow.beans;

public class Sura {

	private int Id;
	private String ArName;
	private String EnName;
	private int AyaCount;
	private int PrevAyat;
	public int getId() {
		return Id;
	}
	public void setId(int id) {
		Id = id;
	}
	public String getArName() {
		return ArName;
	}
	public void setArName(String arName) {
		ArName = arName;
	}
	public String getEnName() {
		return EnName;
	}
	public void setEnName(String enName) {
		EnName = enName;
	}
	public int getAyaCount() {
		return AyaCount;
	}
	public void setAyaCount(int ayaCount) {
		AyaCount = ayaCount;
	}
	public int getPrevAyat() {
		return PrevAyat;
	}
	public void setPrevAyat(int prevAyat) {
		PrevAyat = prevAyat;
	}
	
	
}
